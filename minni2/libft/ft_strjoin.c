#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	s1len;

	if (!s1 || !s2)
		return (NULL);
	str = (char *)malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (str == NULL)
		return (NULL);
	s1len = ft_strlcpy(str, s1, ft_strlen(s1) + 1);
	ft_strlcat(str, s2, s1len + ft_strlen(s2) + 1);
	free((char *)s1);
	return (str);
}
